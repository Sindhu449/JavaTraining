package datatype;

public class DataTypes {
	public static void main(String[] args) {
		
		byte bData = 12;
		int Idata =123;
		short sdata = 12000;
		long ldata = 59876545678L;
		float fdata = 1.10f;
		double ddata= 12.29d;
		boolean bdata = false;
		char cdata = 'A';
		
		
		System.out.println("Value of Byte Data Type is  " + bData);
		System.out.println("Value of Integer Data Type is  " + Idata);
		System.out.println("Value of Short Data Type is  " + sdata);
		System.out.println("Value of Long Data Type is  " + ldata);
		System.out.println("Value of Float Data Type is  " + fdata);
		System.out.println("Value of Double Data Type is  " + ddata);
		System.out.println("Value of Character Data Type is  " + cdata);
		System.out.println("Value of Boolean Data Type is  " + bData);
		}
	}
